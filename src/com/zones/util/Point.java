package com.zones.util;

public class Point {
    private final int x,y,z;
    public Point(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    /**
     * @return the x
     */
    public final int getX() {
        return x;
    }
    /**
     * @return the y
     */
    public final int getY() {
        return y;
    }
    /**
     * @return the z
     */
    public final int getZ() {
        return z;
    }
    
    
}
